package com.mockneat.types;

public class Pair <T1,T2> {

    private T1 first;
    private T2 second;

    public Pair(T1 first, T2 second) {
        this.first = first;
        this.second = second;
    }

    public static <T1, T2> Pair<T1, T2> of(T1 first, T2 second) {
        return new Pair<>(first, second);
    }

    public T1 getFirst() {
        return first;
    }

    public T2 getSecond() {
        return second;
    }

    @Override
    public String toString() {
        return "Pair{" +
                "first=" + first +
                ", second=" + second +
                '}';
    }

    public String toString(String firstLabel, String secondLabel) {
        return "Pair{" +
                firstLabel + "=" + first +
                ", " + secondLabel + "=" + second +
                '}';
    }
}
